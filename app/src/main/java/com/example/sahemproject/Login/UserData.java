package com.example.sahemproject.Login;

public class UserData {
  String User_ID;
  String User_Name;
  String User_Email;
  String User_PhoneNumber;
  String User_Password;
  String User_ConstructionTime;



  public String getUser_ID() {
    return User_ID;
  }

  public void setUser_ID(String user_ID) {
    User_ID = user_ID;
  }

  public String getUser_Name() {
    return User_Name;
  }

  public void setUser_Name(String user_Name) {
    User_Name = user_Name;
  }

  public String getUser_Email() {
    return User_Email;
  }

  public void setUser_Email(String user_Email) {
    User_Email = user_Email;
  }

  public String getUser_PhoneNumber() {
    return User_PhoneNumber;
  }

  public void setUser_PhoneNumber(String user_PhoneNumber) {
    User_PhoneNumber = user_PhoneNumber;
  }

  public String getUser_Password() {
    return User_Password;
  }

  public void setUser_Password(String user_Password) {
    User_Password = user_Password;
  }

  public String getUser_ConstructionTime() {
    return User_ConstructionTime;
  }

  public void setUser_ConstructionTime(String user_ConstructionTime) {
    User_ConstructionTime = user_ConstructionTime;
  }
}
